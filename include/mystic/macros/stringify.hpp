#pragma once

/**
 * @macro MYSTIC_STRINGIFY_HELPER(variable_macro)
 * @brief Implementation helper of stringify macro.
 *
 * @param variable_macro The macro to be stringified.
 *
 * @returns Stringified version of variable_macro.
 */
#define MYSTIC_STRINGIFY_HELPER(variable_macro) #variable_macro

/**
 * @macro MYSTIC_STRINGIFY(variable_macro)
 * @brief Public-facing stringify macro.
 *
 * @param variable_macro The macro to be stringified.
 *
 * @returns Stringified version of variable_macro.
 */
#define MYSTIC_STRINGIFY(variable_macro) MYSTIC_STRINGIFY_HELPER(variable_macro)

